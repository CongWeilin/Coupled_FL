def train_val_dataloader(dataset, idxs, batch_size):
    """
    Returns train, validation and test dataloaders for a given dataset
    and user indexes.
    """
    # split indexes for train, validation, and test (80, 10, 10)
    idxs = list(idxs)
    idxs_train = idxs[:int(0.8*len(idxs))]
    idxs_val = idxs[int(0.8*len(idxs)):]
        
    trainloader = DataLoader(DatasetSplit(dataset, idxs_train),
                             batch_size=batch_size, shuffle=True)
    validloader = DataLoader(DatasetSplit(dataset, idxs_val),
                             batch_size=int(len(idxs_val)/10), shuffle=False)
    return trainloader, validloader
    
class DatasetSplit(Dataset):
    """An abstract Dataset class wrapped around Pytorch Dataset class.
    """

    def __init__(self, dataset, idxs):
        self.dataset = dataset
        self.idxs = [int(i) for i in idxs]

    def __len__(self):
        return len(self.idxs)

    def __getitem__(self, item):
        image, label = self.dataset[self.idxs[item]]
        return torch.tensor(image), torch.tensor(label)
    
def get_dataset(config):
    """ Returns train and test datasets and a user group which is a dict where
    the keys are the user index and the values are the corresponding data for
    each of those users.
    """

    if config['dataset'] == 'cifar':
        data_dir = '../data/cifar/'
        apply_transform = transforms.Compose(
            [transforms.ToTensor(),
             transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))])

        train_dataset = datasets.CIFAR10(data_dir, train=True, download=True,
                                       transform=apply_transform)

        test_dataset = datasets.CIFAR10(data_dir, train=False, download=True,
                                      transform=apply_transform)

        # sample training data amongst users
        if config['noniid_level'] == 0:
            # Sample IID user data from CIFAR10
            user_groups = cifar_iid(train_dataset, config['num_devices'])
        else:
            # Sample Non-IID user data from CIFAR10
            user_groups = cifar_noniid(train_dataset, config['num_devices'], config['noniid_level'])

    elif config['dataset'] == 'mnist' or 'fmnist':
        if config['dataset'] == 'mnist':
            data_dir = '../data/mnist/'
        else:
            data_dir = '../data/fmnist/'

        apply_transform = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize((0.1307,), (0.3081,))])

        train_dataset = datasets.MNIST(data_dir, train=True, download=True,
                                       transform=apply_transform)

        test_dataset = datasets.MNIST(data_dir, train=False, download=True,
                                      transform=apply_transform)

        # sample training data amongst users
        if config['noniid_level'] == 0:
            # Sample IID user data from Mnist
            user_groups = mnist_iid(train_dataset, config['num_devices'])
        else:
            # Sample Non-IID user data from Mnist
            user_groups = mnist_noniid(train_dataset, config['num_devices'], config['noniid_level'])

    return train_dataset, test_dataset, user_groups


def mnist_iid(dataset, num_users):
    """
    Sample I.I.D. client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return: dict of image index
    """
    num_items = int(len(dataset)/num_users)
    dict_users, all_idxs = {}, [i for i in range(len(dataset))]
    for i in range(num_users):
        dict_users[i] = set(np.random.choice(all_idxs, num_items,
                                             replace=False))
        all_idxs = list(set(all_idxs) - dict_users[i])
    return dict_users


def mnist_noniid(dataset, num_users, noniid_level=2):
    """
    Sample non-I.I.D client data from MNIST dataset
    :param dataset:
    :param num_users:
    :return:
    """
    # 60,000 training imgs -->  200 imgs/shard X 300 shards
    num_shards, num_imgs = int(100 * noniid_level), int(600 / noniid_level)
    idx_shard = [i for i in range(num_shards)]
    dict_users = {i: np.array([]) for i in range(num_users)}
    idxs = np.arange(num_shards*num_imgs)
    labels = dataset.train_labels.numpy()

    # sort labels
    idxs_labels = np.vstack((idxs, labels))
    idxs_labels = idxs_labels[:, idxs_labels[1, :].argsort()]
    idxs = idxs_labels[0, :]

    # divide and assign 2 shards/client
    for i in range(num_users):
        rand_set = set(np.random.choice(idx_shard, noniid_level, replace=False))
        idx_shard = list(set(idx_shard) - rand_set)
        for rand in rand_set:
            dict_users[i] = np.concatenate(
                (dict_users[i], idxs[rand*num_imgs:(rand+1)*num_imgs]), axis=0)
    return dict_users

